- (void)configureCell:(UITableViewCell *)cell atIndexPath:(NSIndexPath *)indexPath
{
    if (indexPath.section == 0)
    {
        [[cell textLabel] setText:@"Ms. Awesomesauce"];
    } else {
        [[cell textLabel] setText:@"My Music"];
    }
}

@end
